import Foundation

//문제: 정수 num1, num2가 매개변수 주어집니다. num1과 num2를 곱한 값을 return 하도록 solution 함수를 완성해주세요.
//입출력 예 #1
//num1이 3, num2가 4이므로 3 * 4 = 12를 return합니다.
//입출력 예 #2
//num1이 27, num2가 19이므로 27 * 19 = 513을 return합니다.

func solution(_ num1:Int, _ num2:Int) -> Int {
    return num1 * num2
}
