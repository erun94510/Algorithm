import Foundation

//문제: 정수 num1과 num2가 주어질 때, num1과 num2의 합을 return하도록 soltuion 함수를 완성해주세요.
//입출력 예 #1
//num1이 2이고 num2가 3이므로 2 + 3 = 5를 return합니다.
//입출력 예 #2
//num1이 100이고 num2가 2이므로 100 + 2 = 102를 return합니다.

func solution(_ num1:Int, _ num2:Int) -> Int {
    return num1 + num2
}
